public class Bitwise {
    public static void main(String[] args) {
        int a = 32;
        int b = 12;
        int c;

        c = a & b;
        System.out.println("a & b = "+c);
        c = a | b;
        System.out.println("a | b = "+c);
        c = a^b;
        System.out.println("a ^ b = "+ c);

    }
}
