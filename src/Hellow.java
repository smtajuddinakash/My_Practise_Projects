import java.util.Scanner;

public class Hellow {
    public static void main(String[] args) {

        int x = 5;
        int y = 3;

       System.out.println(x+=y);        //x = x+y  //5+3 = 8
       System.out.println(x-=y);        //x = x-y  //8-3 = 5
       System.out.println(x*=y);        //x = x*y  //5*3 = 15
       System.out.println(x/=y);        //x = x/y  //15/3 = 5
       System.out.println(x%=y);        //x = x%y  //5%3 = 2

    }
}
